package aula6;

public class TestaIf {

	public static void main(String[] args) {
		
		int teste = 1;
		
		if(teste> 5) {
			System.out.println(teste + " é maior que 5");
		}else if (teste > 3) {
			System.out.println(teste + " é maior que 3");
		}else {
			System.out.println(teste + " é igual ou menor que 3");
		}
		
	}

}
